CREATE TABLE customer (
   customer_id VARCHAR(36) NOT NULL,
   first_name VARCHAR(30) NOT NULL,
   last_name VARCHAR(30) NULL,
   nick_name VARCHAR(30) NOT NULL,
   phone_number VARCHAR(15) NOT NULL,
   email VARCHAR(30) NULL,
   country_id VARCHAR(36) NOT NULL,
   province_id VARCHAR(36) NULL,
   district_id VARCHAR(36) NULL,
   sub_district_id VARCHAR(36) NULL,
   village_id VARCHAR(36) NULL,
   city_id VARCHAR(36) NULL,
   postcode INTEGER NULL,
   address  VARCHAR(250) NOT NULL,
   place_of_birth VARCHAR(250) NOT NULL,
   birth_of_date DATE,
   sex VARCHAR(1) NOT NULL,
   id_card_type VARCHAR(1) NOT NULL,
   id_card_number VARCHAR(30) NOT NULL,
   image VARCHAR(1280) NULL,
   outlet_id VARCHAR(36) NOT NULL,
    active BOOLEAN DEFAULT true,
   created_by VARCHAR(36) NULL,
   created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
   updated_by VARCHAR(36) NULL,
   updated_at TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP,
   PRIMARY KEY (customer_id)
 );
